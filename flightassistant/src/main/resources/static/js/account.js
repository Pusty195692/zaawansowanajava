function editButtonOnClick() {
    $("#emailInput").prop('disabled', false);
    $("#nameInput").prop('disabled', false);
    $("#surnameInput").prop('disabled', false);
    $("#imageInput").prop('disabled', false);
    $("#phoneInput").prop('disabled', false);
    $("#button-edit").hide();
    $("#button-save").show();

}